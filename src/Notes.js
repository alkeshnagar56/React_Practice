// Q1. What is react? how it is different from Javascript.
// **React** is a JavaScript library used to build user interfaces, particularly for **single-page applications**. It focuses on creating reusable UI components that manage their own state. Key features include:

// - **Component-Based Architecture**: UI is broken into small, reusable components.
// - **Virtual DOM**: Efficiently updates the real DOM by minimizing changes.
// - **JSX**: Allows writing HTML-like syntax within JavaScript.
// - **One-Way Data Binding**: Data flows in one direction, making it predictable.

// ### Difference from JavaScript:
// - **React** is a library built with **JavaScript**, but it simplifies UI building with components, state management, and declarative views, while **JavaScript** is a general-purpose programming language used for various tasks like web development, server-side scripting, and more. React focuses specifically on UI development.


// Q2. Difference between angular JSON, react JSON, and vue 


// ### 1. **Angular JSON**:
// - **Purpose**: Refers to the `angular.json` configuration file in Angular projects.
// - **Use**: Configures project settings, build options, assets, and environments.
// - **Example**: Manages how Angular apps are built and run (e.g., development vs. production).
  
// ### 2. **React JSON**:
// - **Purpose**: React does not have a specific `react.json` file; it generally involves working with **JSON data** within React apps.
// - **Use**: React fetches and manipulates JSON data from APIs using JavaScript methods like `fetch()` or `axios`.
// - **Example**: Handles API responses in JSON format using `JSON.parse()` and `JSON.stringify()`.

// ### 3. **Vue**:
// - **Purpose**: **Vue.js** is a JavaScript framework like Angular and React, but with its own approach to building UIs.
// - **Use**: Vue handles JSON similarly to React using JavaScript. It provides a more flexible structure with features like directives and reactive data binding.


